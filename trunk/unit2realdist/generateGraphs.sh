#!/bin/bash

echo "*****************************************************************"
echo "*****************************************************************"
echo "Convert all eps files to PDF"
echo "*****************************************************************"
echo "*****************************************************************\n"

ls -1 | grep p$ | while read fn; do
echo "*****************************************************************"
echo "excuting gnuplot $fn"
echo "*****************************************************************"
gnuplot "$fn"
done

ls -1 | grep .eps$ | while read fn2; do
echo "*****************************************************************"
echo "excuting epstopdf $fn2"
echo "*****************************************************************"
epstopdf "$fn2"
done

